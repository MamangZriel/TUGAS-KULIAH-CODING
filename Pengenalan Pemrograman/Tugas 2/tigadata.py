def sum_score():
    nilai1 = float(input("Masukkan nilai pertama: "))
    nilai2 = float(input("Masukkan nilai kedua: "))
    nilai3 = float(input("Masukkan nilai ketiga: "))

    rata_rata = (nilai1 + nilai2 + nilai3) / 3
